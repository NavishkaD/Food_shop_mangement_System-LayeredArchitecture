 private void clearAllText() {
        txtCusID.setText("");
        txtCusName.setText("");
        txtCusContact.setText("");
        txtCusAddess.setText("");
        txtCusID.requestFocus();
        loadAllCustomers();
    }    
    
    
private void loadAllCustomers() {

        DefaultTableModel dtm = (DefaultTableModel) tblCustomer.getModel();
        //dtm.setRowCount(0);

        
            ArrayList<CustomerDTO> allCustomers;
        try {
            allCustomers = new CustomerFormController().getAllCustomers();
            for (CustomerDTO customerDTO : allCustomers) {

                Object[] row = {customerDTO.getCustomerID(), customerDTO.getCustomerName(), customerDTO.getCustomerContacts(), customerDTO.getCustomerAddress()};
                dtm.addRow(row);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(CustomerFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
            

       
    }

