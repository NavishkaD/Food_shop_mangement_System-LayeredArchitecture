    private void SearchtableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchtableKeyReleased
       
        DefaultTableModel table=(DefaultTableModel)tblCustomer.getModel();
        String Search =Searchtable.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr=new TableRowSorter<DefaultTableModel>(table);
        tblCustomer.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(Search));
        
    }//GEN-LAST:event_SearchtableKeyReleased

    private void tblCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomerMouseClicked
        
        int selecedRow=tblCustomer.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel) tblCustomer.getModel();
        
        txtCusID.setText(model.getValueAt(selecedRow, 0).toString());
        txtCusName.setText(model.getValueAt(selecedRow, 1).toString());
        txtCusContact.setText(model.getValueAt(selecedRow, 2).toString());
        txtCusAddess.setText(model.getValueAt(selecedRow, 3).toString());
      
        
    }//GEN-LAST:event_tblCustomerMouseClicked

