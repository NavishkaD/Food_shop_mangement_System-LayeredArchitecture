    private void clearAllText() {
        txtItemCode.setText("");
        txtItemName.setText("");
        txtItemQty.setText("");
        txtItemUnitPrice.setText("");
        txtItemCode.requestFocus();
        loadAllItems();
    }

    private void loadAllItems() {

        DefaultTableModel dtm = (DefaultTableModel) TableItem.getModel();
        dtm.setRowCount(0);

        try {
            ArrayList<ItemDTO> allItems = new ItemFormController().getAllItems();
            for (ItemDTO item : allItems) {
                Object[] row = {item.getItemcode(), item.getItemName(), item.getItemqtyOnHand(), item.getItemunitPrice()};
                dtm.addRow(row);
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ItemFrome.class.getName()).log(Level.SEVERE, null, ex);
        }
    }    
    
    
