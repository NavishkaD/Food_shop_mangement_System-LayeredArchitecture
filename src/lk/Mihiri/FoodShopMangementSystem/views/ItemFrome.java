    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
      
        String  ItemCode =txtItemCode.getText();
        String ItemName= txtItemName.getText();
        int qty  = Integer.parseInt(txtItemQty.getText());
        double unitPrice = Double.parseDouble(txtItemUnitPrice.getText());
        
        ItemDTO itemDTO =new ItemDTO(ItemCode, ItemName, qty, unitPrice);
        
        try {
          boolean addItem = new ItemFormController().addItem(itemDTO);
            if (addItem) {
                JOptionPane.showMessageDialog(this, "Item Saved..!");
                loadAllItems();
            } else {
                JOptionPane.showMessageDialog(this, "Item Not Saved..!");
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ItemFrome.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_btnAddItemActionPerformed

    private void btnEditItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditItemActionPerformed
      
        String  ItemCode =txtItemCode.getText();
        String ItemName= txtItemName.getText();
        int qty  = Integer.parseInt(txtItemQty.getText());
        double unitPrice = Double.parseDouble(txtItemUnitPrice.getText());
        
        ItemDTO item =new ItemDTO(ItemCode, ItemName, qty, unitPrice);
        
        try {
            boolean UpdateItem = new ItemFormController().updateItem(item);
            
            if (UpdateItem) {
                JOptionPane.showMessageDialog(this, "Item Updated..!");
                loadAllItems();
            } else {
                JOptionPane.showMessageDialog(this, "Item Not Updated..!");
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ItemFrome.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnEditItemActionPerformed

