    private void SearchtableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_SearchtableKeyReleased
       
        DefaultTableModel table=(DefaultTableModel)tblOrder.getModel();
        String Search =Searchtable.getText().toLowerCase();
        TableRowSorter<DefaultTableModel> tr=new TableRowSorter<DefaultTableModel>(table);
        tblOrder.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(Search));
        
    }//GEN-LAST:event_SearchtableKeyReleased

    private void tblOrderMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOrderMouseClicked
        
        int selecedRow=tblOrder.getSelectedRow();
        DefaultTableModel model=(DefaultTableModel) tblOrder.getModel();
        
        txtOrderID.setText(model.getValueAt(selecedRow, 0).toString());
        txtOrderName.setText(model.getValueAt(selecedRow, 1).toString());
        txtCustomerID.setText(model.getValueAt(selecedRow, 2).toString());
        
      
        
    }//GEN-LAST:event_tblOrderMouseClicked

