    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
       
        String OrderID=txtOrderID.getText();
        String OrderDate = txtOrderName.getText();
        String customerID=txtCustomerID.getText();
       
        
          OrderDTO Updatecustomer= new OrderDTO(customerID,OrderDate, customerID);
            
          boolean result;
        try {
            result = new OrderFormController().updateOrder(Updatecustomer);
            if (result) {
                JOptionPane.showMessageDialog(this, "Customer Updated..!");
                loadAllOrders();
            } else {
                JOptionPane.showMessageDialog(this, "Customer Not Updated..!");
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(OrderFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
        
        
        
    }//GEN-LAST:event_btnEditActionPerformed

