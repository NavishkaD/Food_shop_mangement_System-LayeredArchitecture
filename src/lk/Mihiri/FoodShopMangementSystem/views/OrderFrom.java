 private void clearAllText() {
        txtOrderID.setText("");
        txtOrderName.setText("");
        txtCustomerID.setText("");
        txtOrderID.requestFocus();
        loadAllOrders();
    }    
    
    
private void loadAllOrders() {

        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
        dtm.setRowCount(0);

        
            ArrayList<OrderDTO> allOrder;
        try {
            allOrder = new OrderFormController().getAllOrder();
            for (OrderDTO order : allOrder) {

                Object[] row = {order.getOid(),order.getDate(),order.getCustomerID()};
                dtm.addRow(row);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(OrderFrom.class.getName()).log(Level.SEVERE, null, ex);
        }
            

       
    }

